CREATE TABLE Continents 
( 
    ContID NUMBER(5), 
    Continent VARCHAR2(15), 
    CONSTRAINT Continents_ContID_pk PRIMARY KEY(ContID) 
);

CREATE TABLE Countries 
( 
    CountryID NUMBER(5), 
    CountryName VARCHAR2(20), 
    ContID  NUMBER(5), 
    CountryCode VARCHAR2(2),
    CONSTRAINT Countries_ContID_fk FOREIGN KEY(ContID) REFERENCES Continents(ContID), 
    CONSTRAINT CountryCode_pk PRIMARY KEY(CountryID),
    CONSTRAINT CountryCode_chk CHECK (CountryCode IN ('IN', 'CN', 'US', 'FR', 'UK', 'GE', 'JP', 'IT', 'SW', 'SK', 'RU', 'NI', 'AU', 'NZ', 'EG', 'MX', 'BR'))
);

CREATE TABLE Car_Makers
( 
    MakerID NUMBER(5), 
    MakerName VARCHAR2(15), 
    FullName VARCHAR2(25), 
    CountryID NUMBER(5), 
    CONSTRAINT CarMakers_CountryID_fk FOREIGN KEY(CountryID) REFERENCES Countries(CountryID), 
    CONSTRAINT CarMakers_MakerID_pk PRIMARY KEY(MakerID) 
);
--
CREATE TABLE Model_Details 
( 
    ModelID NUMBER(5), 
    MakerID NUMBER(5),
    ModelShortDesc VARCHAR2(45), 
    ModelLongDesc VARCHAR2(100), 
    CONSTRAINT ModelDetails_ModelID_pk PRIMARY KEY(ModelID), 
    CONSTRAINT ModelDetails_fk FOREIGN KEY(MakerID) REFERENCES Car_Makers(MakerID) 
);
--
CREATE TABLE Car_Names 
( 
    CarID   NUMBER(5),
    CarName VARCHAR2(100),
    ModelID NUMBER(5),
    MakerID NUMBER(5),
    CONSTRAINT CarNames_CarID_pk PRIMARY KEY(CarID), 
    CONSTRAINT CarNames_ModelID_fk FOREIGN KEY(ModelID) REFERENCES Model_Details(ModelID),
    CONSTRAINT CarNames_MakerID_fk FOREIGN KEY(MakerID) REFERENCES Car_Makers(MakerID) 
);

CREATE TABLE Car_Details 
( 
    CarID NUMBER(5), 
    mpg NUMBER(5,2), 
    cylinders NUMBER(5), 
    edispl NUMBER(10), 
    horsepower NUMBER(10), 
    weight NUMBER(10), 
    accel NUMBER(10,2), 
    year NUMBER(10), 
    CONSTRAINT CarDetails_CarID_pk PRIMARY KEY(CarID), 
    CONSTRAINT CarDetails_CarID_fk FOREIGN KEY(CarID) REFERENCES Car_Names(CarID) 
);
